// Extend the main language.js translations
document.addEventListener('DOMContentLoaded', () => {
    // Merge final project translations with main translations if language.js is loaded
    if (typeof translations !== 'undefined') {
        Object.keys(finalProjectTranslations).forEach(lang => {
            if (translations[lang]) {
                Object.assign(translations[lang], finalProjectTranslations[lang]);
            }
        });
    } else {
        // If language.js hasn't loaded yet, wait for it
        const checkTranslations = setInterval(() => {
            if (typeof translations !== 'undefined') {
                Object.keys(finalProjectTranslations).forEach(lang => {
                    if (translations[lang]) {
                        Object.assign(translations[lang], finalProjectTranslations[lang]);
                    }
                });
                clearInterval(checkTranslations);
            }
        }, 100);
    }
});

